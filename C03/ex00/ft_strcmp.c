int	ft_strcmp(char *s1, char *s2)
{
	int	n;
	int	m;
	int	res;

	n = 0;
	m = 0;
	res = 0;
	while (s1[n])
	{
		m = n;
		if (s2[m])
		{
			if (s1[n] != s2[m])
			{
				res = res + s1[n] - s2[m];
				break ;
			}
		}
		n++;
	}
	return (res);
}
