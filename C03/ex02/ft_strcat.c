char	*ft_strcat(char *dest, char *src)
{
	int	n;
	int	m;

	n = 0;
	m = 0;
	while (dest[n])
	{
		n++;
	}
	while (src[m])
	{
		if (src[m] != '\0')
		{
			dest[n] = src[m];
			n++;
		}
		m++;
	}
	return (dest);
}
