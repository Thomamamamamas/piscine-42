char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	m;

	m = 0;
	while (m < n)
	{
		if (src[m])
		{
			dest[m] = src[m];
		}
		m++;
	}
	dest[n] = '\0';
	return (dest);
}
