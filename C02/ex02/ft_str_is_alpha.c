int	ft_str_is_alpha(char *str)
{
	int	n;

	n = 0;
	if (str[0] == '\0')
	{
		return (0);
	}
	while (str[n])
	{
		if (!(str[n] >= 97 && str[n] <= 122))
		{
			printf("pute");
			return (0);
		}
		n++;
	}
	printf("reussi");
	return (1);
}
