int	ft_str_is_printable(char *str)
{
	int	n;

	n = 0;
	if (str[0] == '\0')
		return (1);
	while (str[n])
	{
		if (str[n] >= 0 && str[n] <= 31)
		{
			return (0);
		}
		n++;
	}
	return (1);
}
